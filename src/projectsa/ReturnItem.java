    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked
        if (selectedRows != null && selectedRows.length > 0) {
            for (int selectedRow : selectedRows) {
                int equipmentID = Integer.parseInt((String) model.getValueAt(selectedRow, 0));
                int confirm = JOptionPane.showConfirmDialog(this, "Confirm to return row " + selectedRowsString + "?" + "\nTotal price: " + totalprice);
                if (confirm == JOptionPane.YES_OPTION) {
                    JOptionPane.showMessageDialog(this, "Order row " + selectedRowsString, "Equipment rental store", JOptionPane.ERROR_MESSAGE);
                    new SaveAccess().returnRental(equipmentID, customerid);
                    model.removeRow(selectedRow);
                }
            }
        }
    }//GEN-LAST:event_jLabel1MouseClicked

