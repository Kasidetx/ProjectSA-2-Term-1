    private void RentTableMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_RentTableMousePressed
        selectedRows = RentTable.getSelectedRows();
        if (selectedRows.length > 0) {
            selectedRowsString = "";
            for (int row : selectedRows) {
                selectedRowsString += row + 1 + " ";
            }
            RowLabel.setText(selectedRowsString);
        }
    }//GEN-LAST:event_RentTableMousePressed

